def get_response(query):
    query = query.lower()
    info = get_college_info()
    
    # BBA queries
    if 'bba' in query or 'business administration' in query:
        if 'eligibility' in query:
            return '\n'.join(info['eligibility']['bba'])
        return '\n'.join(info['courses']['bba'])
    
    # BCA queries
    elif 'bca' in query or ('bachelor' in query and 'computer' in query):
        if 'eligibility' in query:
            return '\n'.join(info['eligibility']['bca'])
        return '\n'.join(info['courses']['bca'])
    
    # BA Economics queries
    elif 'ba economics' in query or ('economics' in query and ('ba' in query or 'bachelor' in query)):
        if 'eligibility' in query:
            return '\n'.join(info['eligibility']['ba_economics'])
        return '\n'.join(info['courses']['ba_economics'])
    
    # MCA queries
    elif 'mca' in query or ('master' in query and 'computer' in query):
        if 'eligibility' in query:
            return '\n'.join(info['eligibility']['mca'])
        return '\n'.join(info['courses']['mca'])
    
    # PGDM queries
    elif 'pgdm' in query:
        if 'international business' in query or 'ib' in query:
            if 'eligibility' in query:
                return '\n'.join(info['eligibility']['pgdm_ib'])
            return '\n'.join(info['courses']['pgdm_ib'])
        elif 'retail' in query:
            if 'eligibility' in query:
                return '\n'.join(info['eligibility']['pgdm_retail'])
            return '\n'.join(info['courses']['pgdm_retail'])
        else:
            if 'eligibility' in query:
                return '\n'.join(info['eligibility']['pgdm'])
            return '\n'.join(info['courses']['pgdm'])
    
    # FPM queries
    elif 'fpm' in query or ('fellow' in query and 'management' in query):
        if 'eligibility' in query:
            return '\n'.join(info['eligibility']['fpm'])
        return '\n'.join(info['courses']['fpm'])
    
    # MBA queries (redirect to PGDM)
    elif 'mba' in query:
        if 'eligibility' in query:
            return '\n'.join(info['eligibility']['pgdm'])
        return '\n'.join(info['courses']['pgdm'])
    
    # About college
    elif any(word in query for word in ['about', 'college', 'jims', 'overview', 'introduction']):
        return '\n'.join(info['about']['college'])
    
    # Admissions
    elif 'admission' in query or 'apply' in query:
        if 'scholarship' in query:
            return '\n'.join(info['admissions']['scholarships'])
        return '\n'.join(info['admissions']['process'])
    
    # Placements
    elif 'placement' in query or 'job' in query or 'career' in query:
        return '\n'.join(info['placements']['overview'])
    
    # Facilities
    elif 'facility' in query or 'infrastructure' in query or 'hostel' in query or 'campus' in query:
        return '\n'.join(info['facilities']['overview'])
    
    # Contact
    elif 'contact' in query or 'address' in query or 'phone' in query or 'location' in query:
        return '\n'.join(info['contact']['info'])
    
    # Course listing
    elif 'course' in query or 'program' in query:
        response = ['Available Courses at JIMS:',
                   '',
                   '📚 Undergraduate Programs:',
                   '1. BBA (Bachelor of Business Administration)',
                   '2. BCA (Bachelor of Computer Applications)',
                   '3. B.A. (Hons.) Economics',
                   '',
                   '📚 Postgraduate Programs:',
                   '4. MCA (Master of Computer Applications)',
                   '5. PGDM (Post Graduate Diploma in Management)',
                   '6. PGDM (International Business)',
                   '7. PGDM (Retail Management)',
                   '8. FPM (Fellow Program in Management)',
                   '',
                   'For detailed information about any course, please ask specifically.',
                   'Example: "Tell me about BBA" or "What is the eligibility for MCA?"']
        return '\n'.join(response)
    
    return None

    

